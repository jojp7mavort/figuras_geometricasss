public class figurasGeometricas {
    String nombre;
    int lados;
    public figurasGeometricas(String nombre, int lados) {}

    public figurasGeometricas() {
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setLados(int lados) {
        this.lados = lados;
    }

    public int getLados() {
        return lados;
    }

    public String getNombre() {
        return nombre;
    }

}